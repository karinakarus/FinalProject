public class data_anak {
    private int id_anak;
    private String nama_anak;
    private String tgl_lhr;
    private String umur;
    private String berat_badan;
    private String hasil;

    public int getId_Anak() {
        return id_anak;
    }
    public void setId_anak(int id_anak) {
        this.id_anak = id_anak;
    }


    public String getNama_anak() {
        return nama_anak;
    }
    public void setNama_anak(String nama_anak) {
        this.nama_anak = nama_anak;
    }

    public String getTgl_lhr() {
        return tgl_lhr;
    }
    public void setTgl_lhr(String tgl_lhr) {
        this.tgl_lhr = tgl_lhr;
    }

    public String getUmur() {
        return umur;
    }
    public void setUmur(String umur) {
        this.umur = umur;
    }

    public String getBerat_badan() {
        return berat_badan;
    }
    public void setBerat_badan(String berat_badan) {
        this.berat_badan = berat_badan;
    }

    public String getHasil() {
        return hasil;
    }
    public void setHasil(String hasil) {
        this.hasil = hasil;
    }
}
